#include <stdio.h>
#include <stdlib.h>

int main()
{
    //右移操作符
    //右移操作有煞N
    //1、算g右移--> 右G，左a原符位
    //2、右移--> 右G，左a0
    //程式默J是算g右移，也就是正的是正的，的是的

    //整档亩M制在w中是以aa的形式Υ娴
    //正档难aa和原a是一拥
    //档难aa是以下面的算方式算的
    //例如 原a：1000,0000,0000,1000
    //     反a：1111,1111,1111,0111   除了符位之外，取反
    //     aa：1111,1111,1111,1000   反a+1

    //於正碚f，原a右移和aa右移Y果是一拥模因樵a和aa一
    //但於刀言不一樱需要^aaDQ才能得到蚀_Y果

    int a = 16; //0000,0000,0001,0000
    int b = a >> 2;//a的二M制迪蛴乙2晌  0000,0000,0000,0100
    printf("b = %d\n",b);
    a = -6;  //aa：1111,1111,1111,1010   原a：1000,0000,0000,0110
    b = a >> 2;  //aa：1111,1111,1111,1110  原a：1000,0000,0000,0010
    printf("b = %d\n",b);


    //左移操作符
    //左G，右a0
    a = -6; //aa：1111,1111,1111,1010   原a：1000,0000,0000,0110
    b = a<<2; //aa：1111,1111,1110,1000  原a：1000,0000,0001,1000
    printf("b = %d\n",b);

    //c（and） &
    //或（or）  |
    //或（exclusive or） ^
    //以上三符就是把整缔DQ成二M制型，然後ξ徊僮
    //注：一α硪底纱萎或的幼骶得到本身
    a = 5; //0101
    b = 3; //0011
    int c = 0;
    c = a^b;  //第一次或  c = 0110
    a = c^b;  //第二次或  a = 0101
    printf("a = %d\n",a);

    //小例}：在不建另一量的情r下，{Qint型的量的值
    a = a^b;
    b = a^b;
    a = a^b;
    printf("小例}：\na = %d\nb = %d\n",a,b);

    //@N方法的好就是一定不超^int的最大值，缺c就是可x性不
    //另一N方法就是加p法
    a = a+b;
    b = a-b;
    a = a-b;
    //但是@N方法有一}就是在做a+b幼r，可能溢出（int）


    //c（&&）和或（||）
    //c有一特性，只要遇到一0，t後面的程式不绦校直接出Y果0


    a = 0;
    b = 2;
    c = 3;
    int d =4;
    int i = a++&&++b&&d++;  //@在绦械r候，是先oa的值，然後再++，因a=0，所有後面c的程式就不绦校直接出Y果
    printf("c：%d %d %d %d\n",a,b,c,d); //1 2 3 4
    i = a++||++b||d++;//在绦械r候，先oa的值，然後再++，因a=1，所以後面或的程式就不绦校直接出Y果
    printf("c：%d %d %d %d\n",a,b,c,d); //2 2 3 4


    //?操作符
    // ? 可以代替if-else硎褂茫例如：
    if (a>b){
        c = a;
    }
    else{
        c = b;
    }
    printf("c1 = %d\n",c);
    //如果用?的就是：
    c = (a>b ? a:b);
    printf("c2 = %d\n",c);


    //逗表_式
    //淖笸右依次绦校整表_式的Y果是最後一表_式的Y果，例如：
    a = 1;
    b = 2;
    c= (a>b,a=a+10,a,b=a+1);
    //第一a>b,绦薪Y果0(假)，第二a=a+10，绦薪Y果a=11
    //第三a，绦薪Y果a=11，第四，b=a+1，绦薪Y果b=12
    //然後把最後的Y果x值oc
    printf("a = %d,b = %d,c = %d\n",a,b,c);//11 12 12









    return 0;
}
